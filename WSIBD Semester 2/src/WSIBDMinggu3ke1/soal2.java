package WSIBDMinggu3ke1;

public class soal2 {
    public static void main(String[] args) {
        int i = 1; 
        
        System.out.println("               Do__While                 ");
        System.out.println("Bilangan kelipatan 2 antara 0 hingga 100:");
        System.out.println("=========================================");

        do {
            System.out.print(i + " "); 
            i *= 2; 
        } while (i <= 100); 
    }
}
