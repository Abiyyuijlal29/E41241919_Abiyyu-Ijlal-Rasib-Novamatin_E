package WSIBDMinggu2Ke1;
public class Soal2Person {
    // Deklarasi atribut
    private String name;
    private int age;

    // Konstruktor
    public Soal2Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    // Metode untuk mendapatkan nama
    public String getName() {
        return name;
    }

    // Metode untuk mendapatkan ID
    public int getAge() {
        return age;
    }
}
